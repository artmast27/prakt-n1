
# pie_time.py

# Зчитування трьох значень t1, t2, t3
t1, t2, t3 = map(int, input("Введіть три значення: ").split())

# Обчислення часу, необхідного для з'їдання пирога
total_time = 1 / (1 / t1 + 1 / t2 + 1 / t3)

# Виведення результату, округленого до 2 знаків після коми
print(f"Час, необхідний для з'їдання пирога: {total_time:.2f} годин")
